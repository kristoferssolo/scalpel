#!/usr/bin/env node
/**
 * Build src/shared/data/regex/vendor/relics/Relic.Gen.ts from poe2.re's relic
 * data (veiset/poe2.re). Unlike Waystone/Tablet, poe2.re publishes NO
 * Generated.Relic.min.json, so we source the committed TS file
 * src/generated/Relic.Gen.ts and eval its array literal (dev-only; scripts/
 * never ships). Relic mods carry no upstream id, so we synthesize a stable
 * content-hash id (survives upstream reordering). Used with attribution.
 * Re-run when upstream relic data changes:
 *   node scripts/build-relic-regex.js
 */
const https = require('node:https')
const fs = require('node:fs')
const path = require('node:path')

const URL = 'https://raw.githubusercontent.com/veiset/poe2.re/main/src/generated/Relic.Gen.ts'
const OUT = path.join(__dirname, '..', 'src', 'shared', 'data', 'regex', 'vendor', 'relics', 'Relic.Gen.ts')

function fetch(url) {
  return new Promise((resolve, reject) => {
    https
      .get(url, { headers: { 'User-Agent': 'Scalpel' } }, (res) => {
        if (res.statusCode === 301 || res.statusCode === 302) return fetch(res.headers.location).then(resolve, reject)
        if (res.statusCode !== 200) {
          res.resume()
          return reject(new Error(`HTTP ${res.statusCode} for ${url}`))
        }
        let data = ''
        res.on('data', (c) => {
          data += c
        })
        res.on('end', () => resolve(data))
      })
      .on('error', reject)
  })
}

/** FNV-1a 32-bit hash -> unsigned int. Stable across runs and platforms. */
function hashId(s) {
  let h = 0x811c9dc5
  for (let i = 0; i < s.length; i++) {
    h ^= s.charCodeAt(i)
    h = Math.imul(h, 0x01000193)
  }
  return h >>> 0
}

async function main() {
  const src = await fetch(URL)
  // Extract the `relicRegex: RelicRegex[] = [ ... ]` array literal and eval it.
  // The literal uses unquoted keys + trailing commas (not valid JSON).
  const match = src.match(/relicRegex:\s*RelicRegex\[\]\s*=\s*(\[[\s\S]*\])/)
  if (!match) throw new Error('could not locate relicRegex array literal in upstream file')
  const raw = new Function(`return ${match[1]}`)()

  const seen = new Set()
  const mods = raw
    .map((m) => {
      const id = hashId(`${m.name}|${m.regex}|${m.affix}`)
      if (seen.has(id)) throw new Error(`hash collision for relic mod "${m.name}" (id ${id})`)
      seen.add(id)
      return { id, name: m.name, regex: m.regex, values: m.values ?? [], ranges: m.ranges ?? [], affix: m.affix }
    })
    .sort((a, b) => a.name.localeCompare(b.name))

  const header = `// Data sourced from poe2.re (https://github.com/veiset/poe2.re)
// Used with permission. All credit to veiset and contributors.
// Generated by scripts/build-relic-regex.js -- do not edit by hand.
// Relic mods have no upstream id; \`id\` is a stable FNV-1a hash of name+regex+affix.

export interface RelicRegex {
  id: number
  name: string
  regex: string
  values: number[]
  ranges: number[][]
  affix: 'PREFIX' | 'SUFFIX'
}

export const relicRegex: RelicRegex[] = `
  fs.mkdirSync(path.dirname(OUT), { recursive: true })
  fs.writeFileSync(OUT, header + JSON.stringify(mods, null, 2) + '\n')
  console.log(`wrote ${OUT} (${mods.length} relic mods)`)
}

main().catch((err) => {
  console.error('[build-relic-regex] failed:', err.message)
  process.exit(1)
})
